export type LanguageCode = 'en' | 'ar' | 'fr' | 'ms' | 'kk' | 'zh';
export type TranslationKeys = keyof typeof translations.en;

export const getTranslation = (language: string, key: string): string => {
  const langCode = getLanguageCode(language);
  const t = translations[langCode as keyof typeof translations] || translations.en;
  return (t as any)[key] || (translations.en as any)[key] || key;
};

export const getLanguageCode = (languageName: string): LanguageCode => {
  const langMap: { [key: string]: LanguageCode } = {
    // Native names (used by LanguageSelector)
    'English': 'en',
    'العربية': 'ar',
    'Français': 'fr',
    'Bahasa Melayu': 'ms',
    'Қазақша': 'kk',
    '中文': 'zh',
    // English names (fallback support for switching back)
    'Arabic': 'ar',
    'French': 'fr',
    'Malay': 'ms',
    'Kazakh': 'kk',
    'Chinese': 'zh',
    // Direct language codes
    'en': 'en',
    'ar': 'ar',
    'fr': 'fr',
    'ms': 'ms',
    'kk': 'kk',
    'zh': 'zh',
  };
  return langMap[languageName] || 'en';
};

export const getLanguageName = (code: LanguageCode): string => {
  const nameMap: { [key in LanguageCode]: string } = {
    'en': 'English',
    'ar': 'العربية',
    'fr': 'Français',
    'ms': 'Bahasa Melayu',
    'kk': 'Қазақша',
    'zh': '中文',
  };
  return nameMap[code] || 'English';
};

export const getCurrentTranslations = (language: string) => {
  const langCode = getLanguageCode(language);
  return translations[langCode as keyof typeof translations] || translations.en;
};

export const isRTL = (language: string): boolean => {
  const langCode = getLanguageCode(language);
  return langCode === 'ar';
};


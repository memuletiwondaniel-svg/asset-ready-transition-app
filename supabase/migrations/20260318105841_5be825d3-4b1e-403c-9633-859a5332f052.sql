
CREATE TABLE public.dms_units (
  id UUID NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  code TEXT NOT NULL,
  unit_name TEXT NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT true,
  display_order INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

ALTER TABLE public.dms_units ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow authenticated read access on dms_units" ON public.dms_units FOR SELECT TO authenticated USING (true);
CREATE POLICY "Allow authenticated insert access on dms_units" ON public.dms_units FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Allow authenticated update access on dms_units" ON public.dms_units FOR UPDATE TO authenticated USING (true) WITH CHECK (true);
CREATE POLICY "Allow authenticated delete access on dms_units" ON public.dms_units FOR DELETE TO authenticated USING (true);


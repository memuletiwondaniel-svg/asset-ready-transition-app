-- Create the dms_projects table
CREATE TABLE public.dms_projects (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code text NOT NULL,
  project_id text,
  project_name text NOT NULL,
  cabinet text,
  is_active boolean NOT NULL DEFAULT true,
  display_order integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.dms_projects ENABLE ROW LEVEL SECURITY;

-- RLS policies for authenticated users
CREATE POLICY "Authenticated users can read dms_projects"
  ON public.dms_projects FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated users can insert dms_projects"
  ON public.dms_projects FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated users can update dms_projects"
  ON public.dms_projects FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

CREATE POLICY "Authenticated users can delete dms_projects"
  ON public.dms_projects FOR DELETE TO authenticated USING (true);

